import java.util.*;
public class Main {
    public static void main(String[] args) {
        HashMap<Integer, String> hashMap = new HashMap<>(); // HashMap 생성

        hashMap.put(1, "h");
        hashMap.put(2, "e");
        hashMap.put(3, "l");
        hashMap.put(5, "l");
        hashMap.put(6, "o");
        System.out.println(hashMap);

        //출력결과
        //{1=h, 2=e, 3=l, 5=l, 6=o}
    }
}

